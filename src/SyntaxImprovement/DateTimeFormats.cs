namespace oledid.SyntaxImprovement
{
	/// <summary>
	/// Contains common DateTime format strings
	/// </summary>
	public static class DateTimeFormats
	{
		/// <summary>
		/// yyyy-MM-dd, "2014-12-13"
		/// </summary>
		public const string yyyy_MM_dd_ISO = "yyyy-MM-dd";

		/// <summary>
		/// HH:mm:ss, "13:37:01"
		/// </summary>
		public const string HH_mm_ss = "HH:mm:ss";

		/// <summary>
		/// HH:mm:ss, "13:37"
		/// </summary>
		public const string HH_mm = "HH:mm";
	}
}